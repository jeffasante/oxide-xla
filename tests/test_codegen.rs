#[test]
fn codegen_output_is_syntactically_complete() {
    // Verify the generated code has matching structure:
    // - starts with comments/imports
    // - has a function definition
    // - ends with a return

    let onnx = load_onnx_model(&fixture_path("linear.onnx")).unwrap();
    let ir = IrGraph::from_onnx(&onnx).unwrap();
    let code = generate_jax_module(&ir).unwrap();

    let lines: Vec<&str> = code.lines().collect();

    // First line should be the header comment.
    assert!(lines[0].starts_with("# Generated"));

    // Should have def forward somewhere.
    let has_def = lines.iter().any(|l| l.contains("def forward"));
    assert!(has_def, "Missing function definition");

    // Should have a return statement.
    let has_return = lines.iter().any(|l| l.trim().starts_with("return"));
    assert!(has_return, "Missing return statement");
}
